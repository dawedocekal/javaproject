public class Operace {
    public void scitani(int numero,int numero3){
        System.out.println(+(numero+numero3));
    }
    public void odcitani(int numero,int numero3){
        System.out.println(+(numero-numero3));
    }
    public void nasobeni(int numero,int numero3){
        System.out.println(+(numero*numero3));
    }
    public void deleni(int numero,int numero3){
        System.out.println(+(numero/numero3));
    }
}
